    private void tablePacienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePacienteMouseClicked

        if (evt.getClickCount() == 2) {

            try {
                int row = tablePaciente.getSelectedRow();

                numAseguradoTextField.setText(getTableDoctor().getValueAt(row, 0).toString());
                nombreTextField.setText(getTableDoctor().getValueAt(row, 1).toString());
                direccionTextField.setText((getTableDoctor().getValueAt(row, 2).toString()));
                edadTextField.setText(getTableDoctor().getValueAt(row, 3).toString());
                datePicker.setDate(SwingUtilities.stringToDate(getTableDoctor().getValueAt(row, 4).toString()));
                correoTextField.setText(getTableDoctor().getValueAt(row, 5).toString());
                telefonoTextField.setText((getTableDoctor().getValueAt(row, 6).toString()));
                profesionTextField.setText(getTableDoctor().getValueAt(row, 7).toString());
                this.numAseguradoTextField.setEnabled(false);
            } catch (ParseException ex) {
                Logger.getLogger(MantPaciente.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_tablePacienteMouseClicked

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        if (validateData()) {

            Paciente obj = new Paciente(
                    Integer.parseInt(this.numAseguradoTextField.getText()),
                    this.nombreTextField.getText(),
                    this.direccionTextField.getText(),
                    this.datePicker.getDate(),
                    this.correoTextField.getText(),
                    Integer.parseInt(this.telefonoTextField.getText()),
                    this.profesionTextField.getText()
            );

            int res = getBo().update(obj);

            SwingUtilities.updateSwitch(res);

        }
        cleanTextFields();

    }//GEN-LAST:event_updateButtonActionPerformed


    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if (numAseguradoTextField.getText().isEmpty()) {
            SwingUtilities.showJOptionPane("Seleccionar paciente a eliminar");
        } else {
            try {
                int numAsegurado = Integer.parseInt(numAseguradoTextField.getText());
                Paciente obj = new Paciente();
                obj.setNumAsegurado(numAsegurado);
                int res = getBo().delete(obj);
                SwingUtilities.deleteSwitch(res);
                cleanTextFields();

            } catch (NumberFormatException nfe) {
                SwingUtilities.showJOptionPane("Ingresar # de asegurado a eliminar (SOLO NUMEROS)");
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed


    private void getByIdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getByIdButtonActionPerformed

        if (SwingUtilities.includesValidChar(1, this.numAseguradoTextField.getText())) {
            int cedula = Integer.parseInt(this.numAseguradoTextField.getText());
            Paciente obj = getBo().getById(cedula);
            List<Paciente> list = new ArrayList<>();
            list.add(obj);
            if (list.get(0).getDireccion() == null) {
                SwingUtilities.showJOptionPane("No hay registros");
                cleanTextFields();
            } else {
                fillTable(list);
            }
        }

    }//GEN-LAST:event_getByIdButtonActionPerformed

    private void getByNameButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getByNameButtonActionPerformed
        if (SwingUtilities.includesValidChar(2, this.nombreTextField.getText())) {
            List<Paciente> list = getBo().getByName(this.nombreTextField.getText());
            if (list.isEmpty()) {
                SwingUtilities.showJOptionPane("No hay registros");
                cleanTextFields();
            } else {
                fillTable(list);
            }
        }


    }//GEN-LAST:event_getByNameButtonActionPerformed


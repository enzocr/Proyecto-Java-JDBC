    private void cleanTextFields() {

        this.numAseguradoTextField.setEnabled(true);

        this.numAseguradoTextField.setText("");
        this.nombreTextField.setText("");
        this.direccionTextField.setText("");
        this.datePicker.setDate(new Date());
        this.correoTextField.setText("");
        this.telefonoTextField.setText("");
        this.profesionTextField.setText("");

        fillTable();
    }


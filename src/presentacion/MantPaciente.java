    private boolean validateData() {

        if (!SwingUtilities.includesValidChar(1, this.numAseguradoTextField.getText())) {
            SwingUtilities.showJOptionPane("Digite cedula del doctor");
            this.numAseguradoTextField.setBorder(SwingUtilities.colorBorder(1));
            return false;
        }
        if (!SwingUtilities.includesValidChar(2, this.nombreTextField.getText())) {
            SwingUtilities.showJOptionPane("Digite nombre del doctor");
            this.nombreTextField.setBorder(SwingUtilities.colorBorder(1));
            return false;
        }
        if (this.correoTextField.getText().isEmpty()) {
            SwingUtilities.showJOptionPane("Digite correo del doctor");
            this.correoTextField.setBorder(SwingUtilities.colorBorder(1));
            return false;
        }
        if (!SwingUtilities.includesValidChar(2, this.profesionTextField.getText())) {
            SwingUtilities.showJOptionPane("Digite especialidad del doctor");
            this.profesionTextField.setBorder(SwingUtilities.colorBorder(1));
            return false;
        }

        if (!SwingUtilities.includesValidChar(1, this.telefonoTextField.getText())) {
            SwingUtilities.showJOptionPane("Digite telefono del doctor");
            this.telefonoTextField.setBorder(SwingUtilities.colorBorder(1));
            return false;
        }

        return true;
    }

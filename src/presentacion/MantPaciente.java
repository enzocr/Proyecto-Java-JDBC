    private void fillTable() {

        this.modelPaciente = new DefaultTableModel() {
            private static final long serialVersionUID = 1L;

            @Override
            public boolean isCellEditable(int row, int colum) {
                return false;
            }
        };
        getTableDoctor().getTableHeader().setReorderingAllowed(false);

        getModelPaciente().setColumnCount(0);

        getModelPaciente().addColumn("# Asegurado");
        getModelPaciente().addColumn("Nombre");
        getModelPaciente().addColumn("Dirección");
        getModelPaciente().addColumn("Edad");
        getModelPaciente().addColumn("Fecha Nacimiento");
        getModelPaciente().addColumn("Correo");
        getModelPaciente().addColumn("Teléfono");
        getModelPaciente().addColumn("Profesión");

        List<Paciente> list = getBo().getAll();

        if (list != null) {
            getModelPaciente().setNumRows(list.size());

            for (int i = 0; i < list.size(); i++) {
                Paciente p = list.get(i);
                getModelPaciente().setValueAt(p.getNumAsegurado(), i, 0);//i=fila 0=columna
                getModelPaciente().setValueAt(p.getNombre(), i, 1);
                getModelPaciente().setValueAt(p.getDireccion(), i, 2);
                getModelPaciente().setValueAt(p.getEdad(), i, 3);
                getModelPaciente().setValueAt(p.getFechaNacimiento(), i, 4);
                getModelPaciente().setValueAt(p.getEmail(), i, 5);
                getModelPaciente().setValueAt(p.getTelefono(), i, 6);
                getModelPaciente().setValueAt(p.getProfesion(), i, 7);
            }
        }

        getTableDoctor().setModel(getModelPaciente());

    }

